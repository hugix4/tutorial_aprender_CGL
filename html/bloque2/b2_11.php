          Ejemplo: <em>la <u>adolescencia</u> es una <u>etapa</u> del <u>desarrollo humano</u> que comienza con la <u>pubertad</u>. </em></p>
        <p>Para construir un mapa conceptual, sigue    los pasos que a continuación se te presentan: </p>
        <div class="col5">
