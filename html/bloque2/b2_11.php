        <p class="subtitulo">¿Cómo elaborar un mapa mental?</p>
        <p>&nbsp;</p>
        <div class="col5">
          <ul>
            <ul>
              <ul style="list-style-image:url(../img/tic.png)">
                <li>
                  <p><span class="resb2chi">Coloca la idea o concepto central del mapa en el centro de la hoja.</span> De preferencia, esta idea debe estar    representada con una imagen clara y poderosa que sintetice el tema general    del mapa. </p>
                </li>
                <li>
                  <p><span class="resb2chi">Dibuja ramas gruesas que salen del centro y se afinan hacia los    extremos.</span></p>
                </li>
                <li>
                  <p><span class="resb2chi">Ubica por medio de una lluvia de ideas, las ideas relacionadas con la idea    central. </span> Escríbelas sobre las ramas que trazaste y    acompaña las palabras con imágenes (todas las que sean posibles). </p>
                </li>
                <li>
                  <p>Subdivide las ramas centrales en ramas más    delgadas que amplíen la información de la idea básica organizadora. </strong>En este caso, procura también acompañar    las ideas escritas con imágenes. <strong></strong></p>
                </li>
              </ul>
            </ul>
          </ul>
          </p>
