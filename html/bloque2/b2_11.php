                  <p><span class="resb2chi">Clasifica los conceptos en distintos niveles</span> de acuerdo con su generalidad o su capacidad de    incluir otros conceptos. En un mapa conceptual los conceptos más generales    deben ubicarse en niveles superiores y los conceptos más específicos en    niveles inferiores.</p>
                </li>
                <li>
                  <p><span class="resb2chi">Ubica el concepto central o más general en la parte superior del mapa</span> y a partir de éste comienza a colocar los    conceptos por niveles, de acuerdo con el orden que determinaste en el paso    anterior.</p>
                </li>
                <li>
                  <p><span class="resb2chi">Establece las relaciones que existen entre los diferentes conceptos </span>(del mismo o distintos niveles) por medio de    líneas o flechas.</p>
                </li>
                <li>
                  <p><span class="resb2chi">Agrega conectores</span> (palabras breves) que te    sirvan para establecer y aclarar la relación que se establece entre    conceptos.</p>
                </li>
                <li>
                  <p><span class="resb2chi">Revisa tu mapa, </span>ya que esto te    permitirá darte cuenta de si la forma en que ubicaste los conceptos es    adecuada, y/o identificar nuevas relaciones (que no habías previsto) entre    los conceptos. </p>
                </li>
              </ul>
            </ul>
